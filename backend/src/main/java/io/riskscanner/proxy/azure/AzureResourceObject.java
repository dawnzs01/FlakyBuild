package io.riskscanner.proxy.azure;

public class AzureResourceObject {

    private String id;
    private String name;
    private String description;

    public AzureResourceObject(){

    }

    public AzureResourceObject(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
